package kr.ac.cropfit.model;

public class Auction {

	int auction_NO;
	int trade_NO;
	String auction_SIZE;
	String auction_ITEM;
	String auction_PRICE;
	String auction_RESULT;
	String auction_STATUS;
	
	
	public int getAuction_NO() {
		return auction_NO;
	}
	public void setAuction_NO(int auction_NO) {
		this.auction_NO = auction_NO;
	}
	public int getTrade_NO() {
		return trade_NO;
	}
	public void setTrade_NO(int trade_NO) {
		this.trade_NO = trade_NO;
	}
	public String getAuction_SIZE() {
		return auction_SIZE;
	}
	public void setAuction_SIZE(String auction_SIZE) {
		this.auction_SIZE = auction_SIZE;
	}
	public String getAuction_ITEM() {
		return auction_ITEM;
	}
	public void setAuction_ITEM(String auction_ITEM) {
		this.auction_ITEM = auction_ITEM;
	}
	public String getAuction_PRICE() {
		return auction_PRICE;
	}
	public void setAuction_PRICE(String auction_PRICE) {
		this.auction_PRICE = auction_PRICE;
	}
	public String getAuction_RESULT() {
		return auction_RESULT;
	}
	public void setAuction_RESULT(String auction_RESULT) {
		this.auction_RESULT = auction_RESULT;
	}
	public String getAuction_STATUS() {
		return auction_STATUS;
	}
	public void setAuction_STATUS(String auction_STATUS) {
		this.auction_STATUS = auction_STATUS;
	}
	

	
	
	
}
