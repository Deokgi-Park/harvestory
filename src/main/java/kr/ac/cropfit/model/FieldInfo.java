package kr.ac.cropfit.model;

public class FieldInfo {
	
	int field_NO;
	String field_OWNER;
	String field_ADDRESS;
	String field_SIZE;
	int doc_NO;
	
	public int getField_NO() {
		return field_NO;
	}
	public void setField_NO(int field_NO) {
		this.field_NO = field_NO;
	}
	public String getField_OWNER() {
		return field_OWNER;
	}
	public void setField_OWNER(String field_OWNER) {
		this.field_OWNER = field_OWNER;
	}
	public String getField_ADDRESS() {
		return field_ADDRESS;
	}
	public void setField_ADDRESS(String field_ADDRESS) {
		this.field_ADDRESS = field_ADDRESS;
	}
	public String getField_SIZE() {
		return field_SIZE;
	}
	public void setField_SIZE(String field_SIZE) {
		this.field_SIZE = field_SIZE;
	}
	public int getDoc_NO() {
		return doc_NO;
	}
	public void setDoc_NO(int doc_NO) {
		this.doc_NO = doc_NO;
	}
	
	
	
}
